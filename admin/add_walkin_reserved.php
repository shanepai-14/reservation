<script>
	  function handleSelectChange() {
            var selectElement = document.getElementById("stayType");
            var room = document.getElementById("room_no");
            var days = document.getElementById("days");

            if (selectElement.value === "Open Time") {
				room.value = "";
				days.value ="";
                room.disabled = true;
				days.disabled = true;
            } else {
                room.disabled = false;
                days.disabled = false;
            }
        }
</script>
